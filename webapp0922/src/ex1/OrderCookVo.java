package ex1;

public class OrderCookVo {
	private MenuVo mvo;
	private int ono, mno, qty;
	private String odate, oname;
	public MenuVo getMvo() {
		return mvo;
	}
	public void setMvo(MenuVo mvo) {
		this.mvo = mvo;
	}
	public int getOno() {
		return ono;
	}
	public void setOno(int ono) {
		this.ono = ono;
	}
	public int getMno() {
		return mno;
	}
	public void setMno(int mno) {
		this.mno = mno;
	}
	public int getQty() {
		return qty;
	}
	public void setQty(int qty) {
		this.qty = qty;
	}
	public String getOdate() {
		return odate;
	}
	public void setOdate(String odate) {
		this.odate = odate;
	}
	public String getOname() {
		return oname;
	}
	public void setOname(String oname) {
		this.oname = oname;
	}
	
}
