    private void startvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startvActionPerformed
        ans.setText("");
        timeLimit();
        qMap = road();
        
        ArrayList<String> list = new  ArrayList<String>();
        for (Map.Entry<String, String> entrySet : qMap.entrySet()) {
            list.add(entrySet.getKey());
        }
       
        quiz.setText(list.get(qlistCount));
        qlistCount++;
        if (qlistCount ==qMap.size()) {
            System.out.println(" 더이상 문제가 없습니다 ! ");
            quiz.setText("더 이상 문제가 없습니다 ! ");
            qlistCount=0;
        }
    }//GEN-LAST:event_startvActionPerformed
    
