    private QustionMap road() {
        ObjectInputStream ois = null;
        try {
            ois = new ObjectInputStream(new FileInputStream(f));
            qMap=(QustionMap) ois.readObject();
        } catch (IOException ex) {
            ex.printStackTrace();
        } catch (ClassNotFoundException ex) {
            ex.printStackTrace();
        }
            return qMap; 
    }  
    
        
    private boolean inputchk;
    private void timeLimit(){
    new Thread(){
            @Override
            public void run() {
                for (int i = 10; i >0; i--) {
                    try {
                    if (inputchk) { // 사용자가 입력했을떄 ! 
                        count.setText("Goood");
                        inputchk = false;
                        return;
                    }
                        count.setText(i+"");
                        Thread.sleep(1000);
                    } catch (InterruptedException ex) {
                        
                    }
                }
                JOptionPane.showMessageDialog(ExamThread.this, "10 초 동안 입력하지 못했습니다 ! ");
                resultTextArea.append("입력 시간이 지났습니다 ");
            }
    }.start();
   
    }
    
 
    
    
